#include <stdio.h>

/**
  * print_diagsums - prints the sum of the
  * two diagonals of a square matrix
  * @a: array to be used
  * @size: size of array
  */

void print_diagsums(int *a, int size)
{
	int i = 0;
	int j;

	for (; j > 0; i++)
	{
		a[i] = size;
		j--;
	}
	putchar('\n');
}
